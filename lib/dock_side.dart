/// Dock side enumerate all possible docking sides in [DockLayout] widget.
enum DockSide {
  /// dock to left side of top pane.
  topLeft,

  /// dock to right side of top pane.
  topRight,

  /// dock to top side of left pane.
  leftTop,

  /// dock to bottom side of left pane.
  leftBottom,

  /// dock to top side of right pane.
  rightTop,

  /// dock to bottom side of right pane.
  rightBottom,

  /// dock to left side of bottom pane.
  bottomLeft,

  /// dock to right side of bottom pane.
  bottomRight
}
